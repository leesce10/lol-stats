// ========== Merge script ==========
const profilesDir = "src/data/champion-profiles";
const files = fs.readdirSync(profilesDir);
let added = 0;
const skipped = [];

for (const file of files) {
  if (!file.endsWith(".json")) continue;
  const key = file.replace(".json", "").replace(/-/g, "");
  const combos = COMBOS[key];
  if (!combos) {
    skipped.push(file);
    continue;
  }

  const filePath = path.join(profilesDir, file);
  const data = JSON.parse(fs.readFileSync(filePath, "utf-8"));

  if (data.keyCombos && data.keyCombos.length > 0) {
    // Already has combos (TF, Zed-mid, Ahri, Darius, Leona) — skip to avoid overwriting
    skipped.push(file + " (already has keyCombos)");
    continue;
  }

  // Insert keyCombos between punishTriggers and phases (preserve order)
  const ordered = {};
  for (const [k, v] of Object.entries(data)) {
    ordered[k] = v;
    if (k === "punishTriggers") {
      ordered.keyCombos = combos;
    }
  }

  fs.writeFileSync(filePath, JSON.stringify(ordered, null, 2), "utf-8");
  added++;
}

console.log(`Added keyCombos to ${added} profiles.`);
if (skipped.length) {
  console.log("Skipped:");
  for (const s of skipped) console.log("  " + s);
}
